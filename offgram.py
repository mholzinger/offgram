class Handler(http.server.BaseHTTPRequestHandler):
    protocol_version = "HTTP/1.1"

    def log_message(self, *a):
        pass

    def handle(self):
        # browsers routinely drop thumbnail/video connections mid-flight;
        # don't dump a traceback for that.
        try:
            super().handle()
        except (ConnectionResetError, BrokenPipeError):
            pass

    def _send(self, code, body, ctype="text/html; charset=utf-8", extra=None):
        self.send_response(code)
        self.send_header("Content-Type", ctype)
        self.send_header("Content-Length", str(len(body)))
        for k, v in (extra or {}).items():
            self.send_header(k, v)
        self.end_headers()
        if self.command != "HEAD":
            self.wfile.write(body)

    def do_GET(self):
        u = urllib.parse.urlparse(self.path)
        path, qs = u.path, urllib.parse.parse_qs(u.query)
        if path == "/":
            return self._send(200, render_index())
        if path.startswith("/p/"):
            b = render_profile(urllib.parse.unquote(path[3:]))
            return self._send(200, b) if b else self._send(404, b"no such profile")
        if path == "/thumb":
            return self.serve_thumb(qs.get("p", [""])[0])
        if path == "/media":
            return self.serve_media(qs.get("p", [""])[0])
        if path == "/caption":
            cap, url = read_post_meta(urllib.parse.unquote(qs.get("p", [""])[0]))
            return self._send(200, json.dumps({"caption": cap, "url": url}).encode(),
                              "application/json")
        if path == "/status":
            with JOBS_LOCK:
                jobs_running = any(j["running"] for j in JOBS.values())
                jobs_payload = {k: {"running": v["running"], "log": v["log"][-20:]}
                                for k, v in JOBS.items()}
            payload = {"any": jobs_running or SCAN["running"],
                       "scan": dict(SCAN), "prewarm": dict(PREWARM),
                       "heartbeat": dict(HEARTBEAT), "jobs": jobs_payload}
            return self._send(200, json.dumps(payload).encode(), "application/json")
        return self._send(404, b"not found")

    do_HEAD = do_GET

    def do_POST(self):
        u = urllib.parse.urlparse(self.path)
        n = int(self.headers.get("Content-Length", 0))
        form = urllib.parse.parse_qs(self.rfile.read(n).decode() if n else "")
        if u.path == "/update":
            p = form.get("profile", [""])[0]
            if p and p in INDEX["profiles"]:
                if not (p in JOBS and JOBS[p]["running"]):
                    threading.Thread(target=run_update, args=([p],), daemon=True).start()
                return self._send(200, b'{"ok":true}', "application/json")
            return self._send(400, b'{"ok":false}', "application/json")
        if u.path == "/update-all":
            ps = [p for p in INDEX["profiles"]
                  if not (p in JOBS and JOBS[p]["running"])]
            threading.Thread(target=run_update, args=(ps,), daemon=True).start()
            return self._send(200, b'{"ok":true}', "application/json")
        if u.path == "/rescan":
            start_scan_thread(None)
            return self._send(200, b'{"ok":true}', "application/json")
        if u.path == "/heartbeat":
            if not HEARTBEAT["running"]:
                p = form.get("profile", [""])[0]
                names = [p] if (p and p in INDEX["profiles"]) else None
                start_heartbeat(names)
            return self._send(200, b'{"ok":true}', "application/json")
        if u.path == "/add":
            p = form.get("profile", [""])[0].strip().lstrip("@").lower()
            if p and re.match(r"^[a-z0-9._]{1,40}$", p):
                if not (p in JOBS and JOBS[p]["running"]):
                    # run_update downloads then scans the new profile into the index
                    threading.Thread(target=run_update, args=([p],), daemon=True).start()
                return self._send(200, b'{"ok":true}', "application/json")
            return self._send(400, b'{"ok":false}', "application/json")
        if u.path == "/account":
            ok = set_login(form.get("login", [""])[0])
            return self._send(200 if ok else 400,
                              b'{"ok":true}' if ok else b'{"ok":false}',
                              "application/json")
        return self._send(404, b"not found")

    def serve_thumb(self, rel):
        t = thumb_for(urllib.parse.unquote(rel))
        if not t or not t.exists():
            gif = (b"GIF89a\x01\x00\x01\x00\x80\x00\x00\x00\x00\x00\xff\xff\xff"
                   b"!\xf9\x04\x01\x00\x00\x00\x00,\x00\x00\x00\x00\x01\x00\x01"
                   b"\x00\x00\x02\x02D\x01\x00;")
            return self._send(200, gif, "image/gif")
        ctype = mimetypes.guess_type(str(t))[0] or "image/jpeg"
        return self._send(200, t.read_bytes(), ctype, {"Cache-Control": "max-age=86400"})

    def serve_media(self, rel):
        full = safe_under_root(urllib.parse.unquote(rel))
        if not full or not full.is_file():
            return self._send(404, b"not found")
        ctype = mimetypes.guess_type(str(full))[0] or "application/octet-stream"
        size = full.stat().st_size
        rng = self.headers.get("Range")
        if rng and rng.startswith("bytes="):
            try:
                s, _, e = rng[6:].partition("-")
                start = int(s) if s else 0
                end = int(e) if e else size - 1
                end = min(end, size - 1)
                start = max(0, min(start, end))
            except Exception:                         # noqa: BLE001
                start, end = 0, size - 1
            length = end - start + 1
            self.send_response(206)
            self.send_header("Content-Type", ctype)
            self.send_header("Accept-Ranges", "bytes")
            self.send_header("Content-Range", "bytes %d-%d/%d" % (start, end, size))
            self.send_header("Content-Length", str(length))
            self.end_headers()
            if self.command == "HEAD":
                return
            with open(full, "rb") as f:
                f.seek(start)
                remaining = length
                while remaining > 0:
                    chunk = f.read(min(65536, remaining))
                    if not chunk:
                        break
                    self.wfile.write(chunk)
                    remaining -= len(chunk)
            return
        self.send_response(200)
        self.send_header("Content-Type", ctype)
        self.send_header("Accept-Ranges", "bytes")
        self.send_header("Content-Length", str(size))
        self.end_headers()
        if self.command == "HEAD":
            return
        with open(full, "rb") as f:
            while True:
                chunk = f.read(65536)
                if not chunk:
                    break
                self.wfile.write(chunk)


