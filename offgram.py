#!/usr/bin/env python3
"""
offgram — an offline web gallery for an instaloader archive.

A tiny, dependency-free local gallery for an Instagram archive downloaded with
instaloader, with "update" buttons that drive instaloader to fetch new content.
The archive can live anywhere — local disk or a network share.

Run:
    python3 offgram.py        # collection path + login come from config.py
then open http://localhost:8077

Design notes:
  * The whole collection is scanned ONCE in a background thread and cached to a
    LOCAL json file (~/.cache/offgram); pages render from that in-memory index
    and never re-walk the archive — which keeps browsing instant even when the
    files live on a network share.
  * Captions/original-post URLs come from instaloader's '<name>.txt' / '.json.xz'
    sidecars, read lazily when you open an item. Video posters are generated by
    ffmpeg on demand and cached locally.
  * Nothing is ever written to the archive by this tool except via instaloader.
    If this script breaks, your folders are untouched and browsable in Finder.
  * Optional legacy support: if a 4K Stogram '.stogram.sqlite' and/or
    '.thumb.stogram/' folders are present, offgram also reads captions/URLs and
    thumbnails from them. A pure instaloader archive never uses any of this.
"""

