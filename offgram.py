def local_db_copy():
    """Copy the legacy 4K Stogram sqlite to local disk (a bulk copy beats
    SQLite's random page reads when the archive is on a network share).
    Re-copies only when size/mtime changed."""
    import shutil
    if not DB_PATH.exists():
        return None
    CACHE_ROOT.mkdir(parents=True, exist_ok=True)
    local = CACHE_ROOT / "stogram.sqlite"
    try:
        src = DB_PATH.stat()
        if (not local.exists() or local.stat().st_size != src.st_size
                or local.stat().st_mtime < src.st_mtime):
            print("copying %s (%.0f MB) to local cache…"
                  % (DB_PATH.name, src.st_size / 1e6))
            shutil.copy2(DB_PATH, local)
        return local
    except Exception as exc:                          # noqa: BLE001
        print("warning: could not copy db locally (%s); reading it in place" % exc)
        return DB_PATH


def load_db_index():
    src = local_db_copy()
    if src is None:
        print("note: %s not found — old-file captions will be blank" % DB_PATH.name)
        return
    try:
        uri = "file:%s?mode=ro&immutable=1" % urllib.parse.quote(str(src))
        con = sqlite3.connect(uri, uri=True)
        for iid, title, web_url, ctime in con.execute(
                "SELECT instagram_id, title, web_url, created_time FROM photos"):
            if iid:
                CAPTIONS[iid] = {"caption": title or "", "url": web_url or "",
                                 "ts": ctime or 0}
        con.close()
        print("loaded %d captions from %s" % (len(CAPTIONS), DB_PATH.name))
    except Exception as exc:                         # noqa: BLE001
        print("warning: could not read %s (%s)" % (DB_PATH.name, exc))


