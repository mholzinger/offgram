def thumb_for(rel):
    full = safe_under_root(rel)
    if not full or not full.exists():
        return None
    # 1. reuse a legacy 4K Stogram thumbnail if present (already tiny, free)
    tdir = full.parent / ".thumb.stogram"
    for cand in (tdir / full.name, tdir / (full.stem + ".jpg")):
        if cand.exists():
            return cand
    # 2. a generated, cached thumbnail — for images AND videos. This is the win:
    #    instaloader images have no .thumb.stogram, so the grid used to serve the
    #    full original (hundreds of KB per tile); a thumb is ~10-15 KB.
    t = cached_thumb(full)
    if t:
        return t
    # 3. no ffmpeg available: fall back to the original image (videos get none)
    return full if full.suffix.lower() in IMAGE_EXT else None


# cap concurrent ffmpeg thumbnailers so fast scrolling can't fork a swarm
_thumb_sem = threading.Semaphore(4)


def cached_thumb(full):
    """A small square JPEG thumbnail for an image or video, generated once with
    ffmpeg and cached locally. Returns the cache path, or None if ffmpeg fails."""
    try:
        THUMB_DIR.mkdir(parents=True, exist_ok=True)
        key = re.sub(r"[^A-Za-z0-9_.-]", "_",
                     str(full.relative_to(ROOT)).replace(os.sep, "__"))
    except Exception:                                 # noqa: BLE001
        return None
    out = THUMB_DIR / (key + ".jpg")
    if out.exists():
        return out
    with _thumb_sem:
        if out.exists():
            return out
        vf = ("scale=%d:%d:force_original_aspect_ratio=increase,crop=%d:%d"
              % (THUMB_PX, THUMB_PX, THUMB_PX, THUMB_PX))
        cmd = ["ffmpeg", "-nostdin"]
        if full.suffix.lower() in VIDEO_EXT:
            cmd += ["-ss", "1"]                        # grab a frame ~1s in
        cmd += ["-i", str(full), "-frames:v", "1", "-vf", vf, "-q:v", "5"]
        fd, tmp = tempfile.mkstemp(dir=str(THUMB_DIR), suffix=".jpg")
        os.close(fd)
        cmd += ["-y", tmp]
        try:
            subprocess.run(cmd, stdout=subprocess.DEVNULL,
                           stderr=subprocess.DEVNULL, timeout=40)
            if os.path.getsize(tmp) > 0:
                os.replace(tmp, out)
        except Exception:                             # noqa: BLE001
            pass
        finally:
            if os.path.exists(tmp):
                os.remove(tmp)
    return out if out.exists() else None


# ---------------------------------------------------------------------------
# Perceptual hashing for merge dedup. We compute a DCT-based pHash of the cached
# thumbnail (videos: their poster frame). pHash is robust to Instagram's per-
# account re-encoding (dHash was not — measured 7-17 bit drift on real reposts),
# so the same photo on two accounts lands within a few bits and dedup groups them
# by Hamming distance (tunable threshold). Hashes are cached (phash.json) and
# built in a background job. Reuses ffmpeg — no new deps.
# ---------------------------------------------------------------------------
PHASH = {}                                 # rel -> 16-hex 64-bit pHash of its thumbnail
PHASH_FILE = CACHE_ROOT / "phash.json"
# DCT-II cosine table: 8 low-freq basis vectors over 32 samples (computed once).
_PH_N, _PH_K = 32, 8
_PH_COS = [[math.cos(math.pi * (2 * n + 1) * k / (2 * _PH_N))
            for n in range(_PH_N)] for k in range(_PH_K)]
PHASHJOB = {"running": False, "done": 0, "total": 0, "current": "",
            "mid": "", "stop": False}


def load_phash():
    global PHASH
    if PHASH_FILE.exists():
        try:
            PHASH = json.loads(PHASH_FILE.read_text(encoding="utf-8"))
        except Exception:                             # noqa: BLE001
            PHASH = {}


def save_phash():
    try:
        CACHE_ROOT.mkdir(parents=True, exist_ok=True)
        PHASH_FILE.write_text(json.dumps(PHASH), encoding="utf-8")
    except Exception:                                 # noqa: BLE001
        pass


def _phash_from_gray32(buf):
    """DCT-II pHash of a 32x32 gray frame: take the top-left 8x8 low-frequency
    block, threshold each coefficient at the block's median (excluding DC) -> 64
    bits. Robust to re-compression/resize. buf is 1024 gray bytes."""
    N, K, cos = _PH_N, _PH_K, _PH_COS
    # separable DCT, keeping only the K lowest frequencies on each axis
    tmp = [[0.0] * K for _ in range(N)]               # rows -> N x K
    for r in range(N):
        base = r * N
        for k in range(K):
            ck = cos[k]
            s = 0.0
            for n in range(N):
                s += ck[n] * buf[base + n]
            tmp[r][k] = s
    flat = []
    for k1 in range(K):                               # cols -> K x K
        ck = cos[k1]
        for k2 in range(K):
            s = 0.0
            for r in range(N):
                s += ck[r] * tmp[r][k2]
            flat.append(s)
    med = sorted(flat[1:])[len(flat[1:]) // 2]         # median, excluding DC term
    bits = 0
    for v in flat:
        bits = (bits << 1) | (1 if v > med else 0)
    return "%016x" % bits


def compute_phash(rel):
    if rel in PHASH:
        return PHASH[rel]
    full = safe_under_root(rel)
    if not full:
        return None
    thumb = cached_thumb(full)                         # poster frame for videos
    if not thumb:
        return None
    try:
        r = subprocess.run(["ffmpeg", "-nostdin", "-i", str(thumb),
                            "-vf", "scale=32:32,format=gray", "-f", "rawvideo", "-"],
                           capture_output=True, timeout=20)
        if len(r.stdout) < 1024:
            return None
        PHASH[rel] = _phash_from_gray32(r.stdout[:1024])
        return PHASH[rel]
    except Exception:                                 # noqa: BLE001
        return None


def hash_merge_members(mid):
    """Background pass: compute (and cache) the dHash for every not-yet-hashed
    item across a merge's members, so the deduped view has full coverage."""
    m = MERGES.get(mid)
    if not m or PHASHJOB["running"]:
        return
    todo = [it["rel"] for it in merge_items(m) if it["rel"] not in PHASH]
    PHASHJOB.update({"running": True, "done": 0, "total": len(todo),
                     "current": "", "mid": mid, "stop": False})

    def one(rel):
        if not PHASHJOB["stop"]:
            compute_phash(rel)
        PHASHJOB["done"] += 1
        if PHASHJOB["done"] % 200 == 0:
            save_phash()

    try:
        with ThreadPoolExecutor(max_workers=6) as ex:
            list(ex.map(one, todo))
        save_phash()
        print("dedupe hashing complete for %s (%d new)" % (mid, len(todo)))
    finally:
        PHASHJOB["running"] = False


def start_phash_job(mid):
    if PHASHJOB["running"]:
        return False
    threading.Thread(target=hash_merge_members, args=(mid,), daemon=True).start()
    return True


def _caption_from_node(node):
    c = node.get("caption")
    if isinstance(c, str) and c.strip():
        return c.strip()
    try:
        return node["edge_media_to_caption"]["edges"][0]["node"]["text"].strip()
    except Exception:                                 # noqa: BLE001
        pass
    cap = (node.get("iphone_struct") or {}).get("caption")
    if isinstance(cap, dict):
        return (cap.get("text") or "").strip()
    if isinstance(cap, str):
        return cap.strip()
    return ""


def read_post_meta(rel):
    """Lazily read caption + original-post URL from instaloader sidecars next to
    the media at rel: the '.json.xz' metadata (caption + shortcode->URL), falling
    back to a '.txt' caption. Carousel children share the post's base sidecar."""
    full = safe_under_root(rel)
    if not full:
        return "", ""
    d, stem = full.parent, full.stem
    base = re.sub(r"_\d+$", "", stem)
    cap, url = "", ""
    for s in (stem, base):
        jx = d / (s + ".json.xz")
        if jx.exists():
            try:
                node = json.loads(lzma.open(jx).read()).get("node", {})
                sc = node.get("shortcode") or (node.get("iphone_struct") or {}).get("code")
                if sc:
                    url = "https://www.instagram.com/p/%s/" % sc
                cap = _caption_from_node(node)
            except Exception:                         # noqa: BLE001
                pass
            break
    if not cap:
        for s in (stem, base):
            t = d / (s + ".txt")
            if t.exists():
                try:
                    cap = t.read_text(encoding="utf-8", errors="replace").strip()
                except Exception:                     # noqa: BLE001
                    pass
                break
    return cap, url


