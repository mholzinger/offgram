def parse_meta(filename):
    m = ID_RE.search(filename)
    iid = m.group(1) if m else None
    info = CAPTIONS.get(iid) if iid else None
    if info and info["ts"]:
        return iid, info["ts"]
    d = DATE_RE.search(filename)
    if d:
        try:
            return iid, int(time.mktime(time.struct_time(
                (int(d.group(1)), int(d.group(2)), int(d.group(3)),
                 int(d.group(4)), int(d.group(5)), int(d.group(6)), 0, 0, -1))))
        except Exception:                             # noqa: BLE001
            pass
    return iid, 0


def profile_items(profile):
    rec = INDEX["profiles"].get(profile)
    if not rec:
        return []
    items = []
    for section in SECTIONS:
        for name, has_sc in rec["sections"].get(section, []):
            rel = name if section == "posts" else "%s/%s" % (section, name)
            rel = "%s/%s" % (profile, rel)
            iid, ts = parse_meta(name)
            info = CAPTIONS.get(iid) if iid else None
            items.append({
                "rel": rel,
                "section": section,
                "kind": "video" if Path(name).suffix.lower() in VIDEO_EXT else "image",
                "caption": (info["caption"] if info else ""),
                "url": (info["url"] if info else ""),
                "ts": ts,
                "sc": 1 if has_sc else 0,
            })
    items.sort(key=lambda x: (x["ts"], x["rel"]), reverse=True)
    return items


