def section_dir(profile, section):
    return ROOT / profile if section == "posts" else ROOT / profile / section


def scan_profile(profile):
    """One deep read of a profile from the archive. Returns its index record."""
    rec = {"counts": {}, "total": 0, "cover": None, "sections": {}, "mtime": 0}
    try:
        rec["mtime"] = (ROOT / profile).stat().st_mtime
    except OSError:
        pass
    # legacy profiles already have 4K Stogram thumbnails; the pre-warm skips them.
    rec["legacy"] = (ROOT / profile / ".thumb.stogram").is_dir()
    cover_key = ""
    for section in SECTIONS:
        d = section_dir(profile, section)
        media, meta_stems = [], set()
        if d.is_dir():
            with os.scandir(d) as it:
                for e in it:
                    if not e.is_file() or is_hidden(e.name):
                        continue
                    low = e.name.lower()
                    if low.endswith(".json.xz"):
                        meta_stems.add(e.name[:-8])      # instaloader metadata sidecar
                    elif low.endswith(".txt"):
                        meta_stems.add(Path(e.name).stem)
                    elif Path(e.name).suffix.lower() in MEDIA_EXT:
                        media.append(e.name)
        # a media file has caption metadata if a .txt/.json.xz sidecar matches its
        # stem or the post's base stem (carousel children share one sidecar).
        entries = []
        for n in media:
            stem = Path(n).stem
            base = re.sub(r"_\d+$", "", stem)
            entries.append([n, (stem in meta_stems or base in meta_stems)])
        rec["sections"][section] = entries
        rec["counts"][section] = len(entries)
        rec["total"] += len(entries)
        if section == "posts":
            for n in media:
                if Path(n).suffix.lower() in IMAGE_EXT and n > cover_key:
                    cover_key, rec["cover"] = n, n
    if rec["cover"] is None:                          # fall back to any image
        for section in SECTIONS:
            for n, _ in rec["sections"][section]:
                if Path(n).suffix.lower() in IMAGE_EXT:
                    rec["cover"] = "%s/%s" % (section, n) if section != "posts" else n
                    break
            if rec["cover"]:
                break
    return rec


