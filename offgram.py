JOBS = {}
JOBS_LOCK = threading.Lock()


# instaloader incremental-update support. Without latest-stamps, --fast-update
# can't recognize 4K Stogram's differently-named files and re-downloads a
# profile's entire history. Seeding the stamp from what offgram already has makes
# even the first update incremental.
STAMP_KEYS = {"posts": "post-timestamp", "story": "story-timestamp",
              "reels": "reels-timestamp", "tagged": "tagged-timestamp"}
STAMP_ISO = "%Y-%m-%dT%H:%M:%S.%f%z"


def seed_stamps(profile):
    """Before a profile's FIRST update, seed latest-stamps from the newest item
    already indexed, so instaloader fetches only newer content rather than
    re-downloading everything already archived (under any naming)."""
    rec = INDEX["profiles"].get(profile)
    if not rec:
        return
    # strict=False tolerates a duplicated section/key (last wins); the try guards
    # any other corruption so a bad stamps file never aborts an update.
    cfg = configparser.ConfigParser(strict=False)
    if STAMPS_FILE.exists():
        try:
            cfg.read(STAMPS_FILE)
        except Exception:                             # noqa: BLE001
            cfg = configparser.ConfigParser(strict=False)
    if cfg.has_section(profile):
        return                      # already tracked by a real update; never override
    newest = {}
    for section, key in STAMP_KEYS.items():
        best = 0
        for name, _ in rec["sections"].get(section, []):
            _, ts = parse_meta(name)
            best = max(best, ts)
        if best:
            newest[key] = best
    if not newest:
        return
    cfg.add_section(profile)
    for key, ts in newest.items():
        cfg.set(profile, key,
                datetime.fromtimestamp(ts, timezone.utc).strftime(STAMP_ISO))
    CACHE_ROOT.mkdir(parents=True, exist_ok=True)
    with open(STAMPS_FILE, "w") as f:
        cfg.write(f)
    job = JOBS.get(profile)               # may be called outside an update job (adopt)
    if job is not None:
        job["log"].append(
            "offgram: seeded latest-stamps from existing archive (%s)"
            % ", ".join("%s=%s" % (k, datetime.fromtimestamp(v, timezone.utc).date())
                        for k, v in newest.items()))


def run_update(profiles):
    for profile in profiles:
        with JOBS_LOCK:
            JOBS[profile] = {"running": True, "log": [], "rc": None}
    for profile in profiles:
        try:
            seed_stamps(profile)
            _run_one(profile)
            # refresh just this profile in the index after download
            try:
                rec = scan_profile(profile)
                with INDEX_LOCK:
                    INDEX["profiles"][profile] = rec
                save_index()
            except Exception:                         # noqa: BLE001
                pass
        except Exception as exc:                       # noqa: BLE001
            try:
                JOBS[profile]["log"].append("offgram: update aborted — %s" % exc)
            except Exception:                          # noqa: BLE001
                pass
        finally:
            # never leave a job falsely "running" if a pass threw before _run_one
            with JOBS_LOCK:
                j = JOBS.get(profile)
                if j and j.get("running"):
                    j["running"] = False
                    if j.get("rc") is None:
                        j["rc"] = -1


# ---------------------------------------------------------------------------
# Refresh engine — a slow, resumable, throttle-aware background job that brings
# profiles current via instaloader, ONE at a time with a gap between each, so a
# whole-archive refresh stays cheap and gentle on Instagram. Progress is tracked
# and survives restarts (resume a leftover queue).
# ---------------------------------------------------------------------------
REFRESH_FILE = CACHE_ROOT / "refresh.json"
REFRESH_INTERVAL = float(os.environ.get("OFFGRAM_REFRESH_INTERVAL", "180"))
REFRESH = {"running": False, "paused": False, "stop": False, "current": "",
           "queue": [], "done": 0, "total": 0, "failed": []}
REFRESH_LOCK = threading.Lock()


def save_refresh():
    try:
        CACHE_ROOT.mkdir(parents=True, exist_ok=True)
        data = {k: REFRESH[k] for k in ("queue", "done", "total", "failed", "paused")}
        REFRESH_FILE.write_text(json.dumps(data), encoding="utf-8")
    except Exception:                                 # noqa: BLE001
        pass


def load_refresh():
    if REFRESH_FILE.exists():
        try:
            d = json.loads(REFRESH_FILE.read_text(encoding="utf-8"))
            for k in ("queue", "done", "total", "failed", "paused"):
                if k in d:
                    REFRESH[k] = d[k]
        except Exception:                             # noqa: BLE001
            pass


def refresh_snapshot():
    return {"running": REFRESH["running"], "paused": REFRESH["paused"],
            "current": REFRESH["current"], "done": REFRESH["done"],
            "total": REFRESH["total"], "queued": len(REFRESH["queue"]),
            "failed": len(REFRESH["failed"])}


def refresh_worker():
    REFRESH["running"], REFRESH["stop"] = True, False
    try:
        while not REFRESH["stop"]:
            while REFRESH["paused"] and not REFRESH["stop"]:
                time.sleep(2)
            if REFRESH["stop"]:
                break
            with REFRESH_LOCK:
                if not REFRESH["queue"]:
                    break
                name = REFRESH["queue"][0]
            REFRESH["current"] = name
            try:
                run_update([name])                    # posts + ephemeral + rescan
            except Exception:                         # noqa: BLE001
                REFRESH["failed"].append(name)
            with REFRESH_LOCK:
                if REFRESH["queue"] and REFRESH["queue"][0] == name:
                    REFRESH["queue"].pop(0)
            REFRESH["done"] += 1
            REFRESH["current"] = ""
            save_refresh()
            waited = 0.0                               # interruptible gap before next
            while waited < REFRESH_INTERVAL and not REFRESH["stop"] and REFRESH["queue"]:
                time.sleep(1)
                waited += 1
    finally:
        REFRESH["running"], REFRESH["current"] = False, ""
        save_refresh()


def start_refresh(profiles=None):
    """Begin a new whole-archive (or subset) refresh; skips heartbeat-dead profiles."""
    if REFRESH["running"]:
        return False
    if profiles is None:
        profiles = [p for p in sorted(INDEX["profiles"], key=str.lower)
                    if HEALTH.get(p, {}).get("status") != "dead"
                    and not is_archived(p) and not is_hidden_profile(p)]
    REFRESH.update({"queue": list(profiles), "total": len(profiles), "done": 0,
                    "failed": [], "paused": False, "stop": False})
    save_refresh()
    threading.Thread(target=refresh_worker, daemon=True).start()
    return True


def resume_refresh():
    """Continue a queue left over from a previous run (e.g. after a restart)."""
    if REFRESH["running"] or not REFRESH["queue"]:
        return False
    REFRESH["paused"], REFRESH["stop"] = False, False
    threading.Thread(target=refresh_worker, daemon=True).start()
    return True


def _il_run(folder, target, dirpat, flags, extra=None):
    """Run one instaloader pass fetching `target` (the current handle) into the
    literal `dirpat` folder path. Streams output into folder's job log. Using a
    literal path (not {target}) keeps a renamed account's content in its original
    folder and avoids instaloader nesting highlights by collection title."""
    cmd = list(INSTALOADER_CMD) + INSTALOADER_COMMON
    cmd += ["--dirname-pattern=" + dirpat, "--latest-stamps", str(STAMPS_FILE)]
    cmd += list(flags) + list(extra or [])
    if current_login():
        cmd += ["--login", current_login()]
    cmd += [target]
    log = JOBS[folder]["log"]
    log.append("$ " + " ".join(cmd))
    try:
        proc = subprocess.Popen(
            cmd, cwd=str(ROOT), stdin=subprocess.DEVNULL,
            stdout=subprocess.PIPE, stderr=subprocess.STDOUT, text=True, bufsize=1)
        for line in proc.stdout:
            line = line.rstrip("\n")
            if line:
                log.append(line)
                if len(log) > 500:
                    del log[:len(log) - 500]
        proc.wait()
        return proc.returncode
    except Exception as exc:                          # noqa: BLE001
        log.append("error: %s" % exc)
        return -1


def _run_one(folder):
    log = JOBS[folder]["log"]
    target = identity_handle(folder)                  # current live handle
    if target != folder:
        log.append("--- renamed: fetching @%s into %s/ ---" % (target, folder))
    rc = _il_run(folder, target, folder, [], INSTALOADER_EXTRA)   # posts -> folder/
    if GRAB_EPHEMERAL:
        for sub, flags in EPHEMERAL_PASSES:
            time.sleep(PASS_DELAY)                     # space out passes to avoid throttling
            log.append("--- fetching %s ---" % sub)
            _il_run(folder, target, folder + "/" + sub, flags)   # -> folder/<sub>/
    with JOBS_LOCK:
        JOBS[folder]["running"] = False
        JOBS[folder]["rc"] = rc
    log.append("--- finished (exit %s) ---" % rc)


def any_running():
    with JOBS_LOCK:
        return any(j["running"] for j in JOBS.values())


